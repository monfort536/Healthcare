import mongoose from "mongoose";
const cakeData = new mongoose.Schema({
  cakeName: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  price: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  }
});
//userdetail is the modelname.using these userdetail we can able to create,read,update,delete datas in userdetails collection
const Cake = mongoose.model("cakes", cakeData);

export default Cake;
